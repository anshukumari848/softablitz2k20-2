    void categoryitems()
    {
        try{
            String url="jdbc:mysql://localhost:3306/registration_details";
            String uname="root";
            String password="webster";
            con=DriverManager.getConnection(url,uname,password);
            String query="SELECT * FROM category WHERE user='"+user+"'";
            ps=con.prepareStatement(query);
            ResultSet rs;
            rs=ps.executeQuery();
            txt_category.removeAllItems();
            
            while(rs.next())
            {
                txt_category.addItem(rs.getString("category"));
            }
    }
         catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }

    }
    
    void update_table()
{
    try{
            String url="jdbc:mysql://localhost:3306/registration_details";
            String uname="root";
            String password="webster";
            con=DriverManager.getConnection(url,uname,password);
            String query="SELECT * FROM product WHERE user='"+user+"'";
            ps=con.prepareStatement(query);
            ResultSet rs;
            rs=ps.executeQuery();
            
            DefaultTableModel d= (DefaultTableModel)contents.getModel();
            d.setRowCount(0);
            
            while(rs.next())
            {   Vector v = new Vector();
                v.add( rs.getString("Id"));
                v.add( rs.getString("Product"));
                v.add( rs.getString("Price"));
                v.add( rs.getString("Quantity"));
                v.add( rs.getString("Category"));
                v.add( rs.getString("Mfg_Date"));
                v.add( rs.getString("Expiry_Date"));
                d.addRow(v);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        
    
}

    private void contentsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_contentsMouseClicked
        // TODO add your handling code here:
        DefaultTableModel d1= (DefaultTableModel)contents.getModel();
        int select;
        select = contents.getSelectedRow();
        txt_pname.setText(d1.getValueAt(select,1).toString());
        txt_price.setText(d1.getValueAt(select,2).toString());
        txt_quantity.setText(d1.getValueAt(select,3).toString());
        txt_category.setSelectedItem(d1.getValueAt(select,4).toString());
        txt_mfgdate.setText(d1.getValueAt(select,5).toString());
        txt_expirydate.setText(d1.getValueAt(select,6).toString());
    }//GEN-LAST:event_contentsMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel d1= (DefaultTableModel)contents.getModel();
        int select;
        select = contents.getSelectedRow();
        int id = Integer.parseInt(d1.getValueAt(select,0).toString());
        int result = JOptionPane.showConfirmDialog(null,"Do you want to update the details of the selected item","Warning",JOptionPane.YES_NO_OPTION);
        
        if(result==JOptionPane.YES_OPTION)
        {
         try{
            String url="jdbc:mysql://localhost:3306/registration_details";
            String uname="root";
            String password="webster";
            con=DriverManager.getConnection(url,uname,password);
            String query="update product set Product=?,Price=?,Quantity=?,Category=?,Mfg_Date=?,Expiry_Date=? where id=?";
            ps=con.prepareStatement(query);
        ps.setString(1,txt_pname.getText());
        ps.setString(2,txt_price.getText());
        ps.setString(3,txt_quantity.getText());
        ps.setString(4,txt_category.getSelectedItem().toString());
        ps.setString(5,txt_mfgdate.getText());
        ps.setString(6,txt_expirydate.getText());
        ps.setInt(7,id);
        ps.executeUpdate();
        JOptionPane.showMessageDialog(null,"Details updated");
        update_table();
        txt_pname.setText("");
        txt_price.setText("");
        txt_quantity.setText("");
        txt_category.setSelectedItem(-1);
        txt_mfgdate.setText("");
        txt_expirydate.setText("");}
          catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

